export function getWatch(slug: string): Watch | undefined {
  return WATCHES.find((w) => w.slug === slug);
}

export function watchesByBrand(brandSlug: string): Watch[] {
  return WATCHES.filter((w) => w.brandSlug === brandSlug);
}

export function relatedWatches(slug: string, count = 3): Watch[] {
  const w = getWatch(slug);
  if (!w) return WATCHES.slice(0, count);
  // same brand first, then any others
  const same = WATCHES.filter((x) => x.brandSlug === w.brandSlug && x.slug !== slug);
  const others = WATCHES.filter((x) => x.brandSlug !== w.brandSlug && x.slug !== slug);
  return [...same, ...others].slice(0, count);
}
